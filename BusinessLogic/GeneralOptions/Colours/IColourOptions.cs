    public interface IColourOptions
    {
        /// <summary>
        /// Gets or sets the colours header background.
        /// </summary>
        string ColoursHeaderBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours header breadcrumb text.
        /// </summary>
        string ColoursHeaderBreadcrumbText { get; set; }

        /// <summary>
        /// Gets or sets the colours page title text.
        /// </summary>
        string ColoursPageTitleText { get; set; }

        /// <summary>
        /// Gets or sets the colours section heading underline.
        /// </summary>
        string ColoursSectionHeadingUnderline { get; set; }

        /// <summary>
        /// Gets or sets the colours section heading text.
        /// </summary>
        string ColoursSectionHeadingText { get; set; }

        /// <summary>
        /// Gets or sets the colours page options background.
        /// </summary>
        string ColoursPageOptionsBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours page options text.
        /// </summary>
        string ColoursPageOptionsText { get; set; }

        /// <summary>
        /// Gets or sets the colours table header background.
        /// </summary>
        string ColoursTableHeaderBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours table header text.
        /// </summary>
        string ColoursTableHeaderText { get; set; }

        /// <summary>
        /// Gets or sets the colours tab option background.
        /// </summary>
        string ColoursTabOptionBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours tab option text.
        /// </summary>
        string ColoursTabOptionText { get; set; }

        /// <summary>
        /// Gets or sets the colours row background.
        /// </summary>
        string ColoursRowBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours row text.
        /// </summary>
        string ColoursRowText { get; set; }

        /// <summary>
        /// Gets or sets the colours alternate row background.
        /// </summary>
        string ColoursAlternateRowBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours alternate row text.
        /// </summary>
        string ColoursAlternateRowText { get; set; }

        /// <summary>
        /// Gets or sets the colours field text.
        /// </summary>
        string ColoursFieldText { get; set; }

        /// <summary>
        /// Gets or sets the colours menu option standard text.
        /// </summary>
        string ColoursMenuOptionStandardText { get; set; }

        /// <summary>
        /// Gets or sets the colours menu option hover text.
        /// </summary>
        string ColoursMenuOptionHoverText { get; set; }

        /// <summary>
        /// Gets or sets the colours tooltip background.
        /// </summary>
        string ColoursTooltipBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours tooltip text.
        /// </summary>
        string ColoursTooltipText { get; set; }

        /// <summary>
        /// Gets or sets the colours green light field.
        /// </summary>
        string ColoursGreenLightField { get; set; }

        /// <summary>
        /// Gets or sets the colours green light section text.
        /// </summary>
        string ColoursGreenLightSectionText { get; set; }

        /// <summary>
        /// Gets or sets the colours green light section background.
        /// </summary>
        string ColoursGreenLightSectionBackground { get; set; }

        /// <summary>
        /// Gets or sets the colours green light section underline.
        /// </summary>
        string ColoursGreenLightSectionUnderline { get; set; }
    }
}
