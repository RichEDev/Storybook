/// <summary>
/// Type of access role (add/edit/delete/view)
/// </summary>
public enum AccessRoleType
{
    /// <summary>
    /// Edit access for the specified element
    /// </summary>
    Edit,
    /// <summary>
    /// Add access for the specified element
    /// </summary>
    Add,
    /// <summary>
    /// Delete access for the specified element
    /// </summary>
    Delete,
    /// <summary>
    /// View access for the specified element
    /// </summary>
    View
}