namespace SpendManagementLibrary
{
    /// <summary>
    /// The grouping output type.
    /// </summary>
    public enum GroupingOutputType
    {
        /// <summary>
        /// All fields, wether grouped or not.
        /// </summary>
        All = 0,

        /// <summary>
        /// Only un grouped fields
        /// </summary>
        UnGroupedOnly = 1,

        /// <summary>
        /// Only grouped fields
        /// </summary>
        GroupedOnly = 2
    }
}