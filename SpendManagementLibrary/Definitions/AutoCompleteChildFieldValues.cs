namespace SpendManagementLibrary
{
    /// <summary>
    /// The auto complete child field values.
    /// </summary>
    public class AutoCompleteChildFieldValues
    {
        /// <summary>
        /// Gets or sets the field to build.
        /// </summary>
        public string FieldToBuild { get; set; }

        /// <summary>
        /// Gets or sets the key.
        /// </summary>
        public int Key { get; set; }

        /// <summary>
        /// Gets or sets the value.
        /// </summary>
        public string Value { get; set; }

        /// <summary>
        /// Gets or sets the formatted text.
        /// </summary>
        public string FormattedText { get; set; }
    }
}